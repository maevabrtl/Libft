#include "libft.h"

char	*strchr(const char *s, int c)
{
	char	ch;
	int		i;

	ch = c;
	i = 0;
	while (s)
	{
		if (s[i] == ch)
			return (&s[i]);
		i++;
	}
	return (NULL);
}
